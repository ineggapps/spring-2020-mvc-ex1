package com.sp.test3;

public class User {
	private String name;
	private String tel;
	private int age;

	public String getName() {
		return name;
	}

	public String getTel() {
		return tel;
	}

	public int getAge() {
		return age;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}

	public void setAge(int age) {
		this.age = age;
	}

}
