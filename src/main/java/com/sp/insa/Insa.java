package com.sp.insa;

public class Insa {
	private String name;
	private String birth;
	private String tti;
	private int age;
	private String phone;
	private int pay;
	private int sudang;
	private int tax;
	private int spay;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBirth() {
		return birth;
	}
	public void setBirth(String birth) {
		this.birth = birth;
	}
	public String getTti() {
		return tti;
	}
	public void setTti(String tti) {
		this.tti = tti;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public int getPay() {
		return pay;
	}
	public void setPay(int pay) {
		this.pay = pay;
	}
	public int getSudang() {
		return sudang;
	}
	public void setSudang(int sudang) {
		this.sudang = sudang;
	}
	public int getTax() {
		return tax;
	}
	public void setTax(int tax) {
		this.tax = tax;
	}
	public int getSpay() {
		return spay;
	}
	public void setSpay(int spay) {
		this.spay = spay;
	}
}
